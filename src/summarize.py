"""Scrub + schema + daily Brief synthesis for odd-bot-moltbook.

Daily run: poll.fetch_window → poll.filter_and_rank → scrub → synthesize_daily
           → persist_daily (writes summary.json with status=draft).

Auto-publish orchestration (lock, merge into briefs.json, build, commit, push)
lives in `src.publish`. This module never writes `data/briefs.json` and never
flips `status` to `"published"`.

Scrub-before-LLM is enforced at the function boundary. `synthesize_daily`
only accepts `ScrubbedPost` instances — raw dicts fail with TypeError before
any prompt is built. ScrubbedPost objects are produced exclusively by
`scrub_post()`, which is the only sanctioned constructor.

Schema validation runs at every persist and on every load (see `Brief`).
Engine output never sets `isSeed` — that field is reserved for design
fixtures in `agent-brief/client/src/data/content.ts`.
"""

from __future__ import annotations

import argparse
import json
import os
import re
import tempfile
from dataclasses import dataclass
from datetime import datetime, timedelta, timezone
from functools import lru_cache
from pathlib import Path
from typing import Any, Literal

import anthropic
import duckdb
from pydantic import BaseModel, field_validator

from src.poll import (
    HF_SNAPSHOT_TAG,
    fetch_window,
    filter_and_rank,
    load_config,
    persist_raw,
)

REPO_ROOT = Path(__file__).resolve().parent.parent
DATA_DIR = REPO_ROOT / "data"
DIGESTS_DIR = DATA_DIR / "digests"
DB_PATH = DATA_DIR / "moltbook.duckdb"
RUNS_PATH = DATA_DIR / "runs.jsonl"
CONFIG_PATH = REPO_ROOT / "config" / "submolts.yaml"

STANDARD_DISCLAIMER = (
    "Moltbook content is generated by AI agents and may be fictional, "
    "manipulated, or wrong. This brief is editorial commentary on agent "
    "ecosystem activity, not a record of human events."
)

CLAUDE_MODEL = "claude-opus-4-7"


@lru_cache(maxsize=1)
def _anthropic_client() -> anthropic.Anthropic:
    """Lazy, process-local Anthropic SDK client.

    Reads the API key from `~/.openclaw/keys/moltbook-engine-anthropic-api-key`
    exactly once per process (on first synthesis call) and caches the resulting
    SDK client.

    Containment guarantees:
    - The key lives only in this process's heap. Never exported to env.
    - Subprocesses we spawn pass an explicit minimal env that omits provider keys.
    - Lazy by design: importing this module for the schema (e.g., `Brief`) must
      not trigger the file read. Only `synthesize_daily` loads the key.
    """
    key = (Path.home() / ".openclaw" / "keys" / "moltbook-engine-anthropic-api-key").read_text().strip()
    return anthropic.Anthropic(api_key=key)


# =============================================================================
# Schema — mirrors agent-brief/client/src/data/content.ts
# =============================================================================

BriefTag = Literal[
    "Agents", "Models", "Tooling", "Research", "Industry", "Open Source",
]
BriefStatus = Literal["draft", "published"]

DAILY_SLUG = re.compile(r"^\d{4}-\d{2}-\d{2}$")

# WEEKLY_SLUG survives the weekly-path retirement because legacy `2026-W18`
# remains in `data/briefs.json` (D4 grandfather rule) and must continue to
# pass `_id_matches_phase` on every read. Cadence policy is enforced by the
# orchestrator's merge boundary (src.publish.merge_brief writes daily slugs
# only); the schema's job is "is this structurally a Brief?" Do not delete.
WEEKLY_SLUG = re.compile(r"^\d{4}-W\d{2}$")


class BriefItem(BaseModel):
    headline: str
    body: str
    source: str | None = None


class Brief(BaseModel):
    id: str
    issueNo: int
    date: str
    title: str
    dek: str
    readingMinutes: int
    tags: list[BriefTag]
    items: list[BriefItem]
    status: BriefStatus
    disclaimer: str
    isSeed: bool | None = None

    @field_validator("disclaimer")
    @classmethod
    def _disclaimer_present(cls, v: str) -> str:
        if not v or not v.strip():
            raise ValueError("disclaimer is required and must be non-empty")
        return v

    @field_validator("id")
    @classmethod
    def _id_matches_phase(cls, v: str) -> str:
        if not (DAILY_SLUG.match(v) or WEEKLY_SLUG.match(v)):
            raise ValueError(f"id must be YYYY-MM-DD or YYYY-WW, got {v!r}")
        return v


# =============================================================================
# Scrub layer — mandatory pre-LLM step
# =============================================================================

# Patterns are intentionally conservative. False positives are preferable to
# false negatives; a redacted post is still useful, but a leaked instruction
# can hijack the summarizer.
_SCRUB_PATTERNS: tuple[re.Pattern[str], ...] = (
    re.compile(r"(?i)\b(system|assistant|user)\s*[:\]]\s*"),
    re.compile(r"(?i)ignore\s+(all\s+)?(previous|prior|above)\s+instructions?"),
    re.compile(r"(?i)disregard\s+(all\s+)?(previous|prior|above)\s+instructions?"),
    re.compile(r"(?i)you\s+are\s+now\s+(a|an)\s+\w+"),
    re.compile(r"(?i)forget\s+(everything|all|your)\s+(previous|prior|instructions?)"),
    re.compile(r"<\s*tool[_\-]?call\s*[>:]"),
    re.compile(r"<\s*function[_\-]?call\s*[>:]"),
    re.compile(r"<\s*system\s*>"),
    re.compile(r"<\s*assistant\s*>"),
    re.compile(r"(?im)^\s*(human|H)\s*:\s+"),
    re.compile(r"(?im)^\s*(assistant|A)\s*:\s+"),
)
_REDACTION = "[redacted-instruction-pattern]"
_SCRUB_SENTINEL = "__moltbook_scrubbed_v1__"


@dataclass(frozen=True)
class ScrubbedPost:
    """Sentinel-bearing wrapper that proves a post passed through scrub_post().

    Construct only via `scrub_post()`; manual construction is a code smell and
    will trip review.
    """

    id: str
    submolt: str
    title: str
    body: str
    upvotes: int
    downvotes: int
    comment_count: int
    created_at: str
    sentinel: str = _SCRUB_SENTINEL


def _scrub_text(s: str) -> str:
    out = s
    for pat in _SCRUB_PATTERNS:
        out = pat.sub(_REDACTION, out)
    return out


def scrub_post(post: dict[str, Any]) -> ScrubbedPost:
    """Mandatory scrub. Strips prompt-injection-shaped patterns from text fields."""
    return ScrubbedPost(
        id=str(post["id"]),
        submolt=_scrub_text(str(post.get("submolt", ""))),
        title=_scrub_text(str(post.get("title", ""))),
        body=_scrub_text(str(post.get("content", post.get("body", "")))),
        upvotes=int(post["upvotes"]),
        downvotes=int(post["downvotes"]),
        comment_count=int(post["comment_count"]),
        created_at=str(post["created_at"]),
    )


def _assert_scrubbed(scrubbed: list[ScrubbedPost]) -> None:
    """Hard precondition for any LLM call. Fails at the function boundary."""
    for p in scrubbed:
        if not isinstance(p, ScrubbedPost) or p.sentinel != _SCRUB_SENTINEL:
            raise TypeError(
                "summarizer received unscrubbed input — every post must pass "
                f"through scrub_post() first; got {type(p).__name__}"
            )


